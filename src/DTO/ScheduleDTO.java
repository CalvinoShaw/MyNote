package DTO;

public class ScheduleDTO {
	private String s_id;
	private String s_name;
	private int s_type;
	private String s_content;
	private int s_status;
	private String s_deadline;
	private int s_progress;
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public int getS_type() {
		return s_type;
	}
	public void setS_type(int s_type) {
		this.s_type = s_type;
	}
	public String getS_content() {
		return s_content;
	}
	public void setS_content(String s_content) {
		this.s_content = s_content;
	}
	public int getS_status() {
		return s_status;
	}
	public void setS_status(int s_status) {
		this.s_status = s_status;
	}
	public String getS_deadline() {
		return s_deadline;
	}
	public void setS_deadline(String s_deadline) {
		this.s_deadline = s_deadline;
	}
	public int getS_progress() {
		return s_progress;
	}
	public void setS_progress(int s_progress) {
		this.s_progress = s_progress;
	}
	
}
